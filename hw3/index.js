// Пользователь вводит с клавиатуры число, если ввёл текст, необходимо вывести что значение задано неверно
// Создать фукнцию, которая высчитывает 13% от данного числа и выводит в консоль текст "Размер заработной платы за вычетом налогов равен значение"

const salary = Number(prompt("Введите свою зарплату"));
while (isNaN(salary)) {
  alert("Значение задано неверно. Надо ввести число.");
  salary = Number(prompt("Введите вашу зарплату числом!"));
}
const salaryAfterTaxes = salary - salary * 0.13;
alert(
  `Размер заработной платы за вычетом налогов равен значению ${salaryAfterTaxes} руб.`
);

